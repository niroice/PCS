<h2 style="padding-top: 0; margin-top: 10; padding-bottom: 0; margin-bottom: 10;">About the New Store Claim Page</h2>

<p style="margin-top: 10px; margin-bottom: 10px;"> This page allows staff to create a new store return or financial claim for products. Return claims are for products being returned to the supplier for a financial re-imbursement, while Financial claims are seeking a re-imbursement for products that do not need to be returned to the supplier.</p>

<p style="margin-top: 10px;">To get started creating a new store claim the staff member simply needs to select the claim type, select the current status of the claim and start adding products to the claim by entering a keycode and clicking the Add button. Products can be updated by selecting the product from the list, then changing the quantity and clicking the update button. Products can be removed from the claim by clicking the remove button. Once finished, the staff member must click submit to save the claim or the cancel button to cancel the claim. </p>

