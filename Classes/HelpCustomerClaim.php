<h2 style="padding-top: 0; margin-top: 10; padding-bottom: 0; margin-bottom: 10;">About the Customer Claim Page</h2>
<p style="margin-top: 10px; margin-bottom: 10px;"> This page allows service desk staff to start the process of creating a customer claim for a faulty or broken product.  Entering a product’s unique identification number (keycode) and a receipt’s transaction ID, the system will determine what course of action the staff member must take when handling the claim based on factors such as the supplier’s return policy, the product’s warranty period and the purchase date of the product. The result of the search will then be displayed to the screen informing the staff member on what they need to do next, as well as display relevant options to complete the claim. These options will vary based on the claim type available; examples: a repair claim will display customer and repair fields, a financial claim will require a submission of the claim and non-claimable products will require no action.</p>
